    circuit.append(cirq.T(qubits[8]))
    circuit.append(cirq.H(qubits[4]))
    circuit.append(cirq.T(qubits[10]))
    circuit.append(cirq.CNOT(qubits[8], qubits[3]))
    circuit.append(cirq.H(qubits[1]))
    circuit.append(cirq.T(qubits[10]))
    circuit.append(cirq.CNOT(qubits[6], qubits[0]))
    circuit.append(cirq.T(qubits[0]))
    circuit.append(cirq.CNOT(qubits[5], qubits[2]))
    circuit.append(cirq.T(qubits[10]))
    circuit.append(cirq.H(qubits[7]))
    circuit.append(cirq.H(qubits[1]))
    circuit.append(cirq.CNOT(qubits[2], qubits[4]))
    circuit.append(cirq.H(qubits[1]))
    circuit.append(cirq.H(qubits[1]))
    circuit.append(cirq.T(qubits[2]))
    circuit.append(cirq.S(qubits[1]))
    circuit.append(cirq.S(qubits[3]))
    circuit.append(cirq.T(qubits[3]))
    circuit.append(cirq.CNOT(qubits[7], qubits[2]))
    circuit.append(cirq.S(qubits[10]))
    circuit.append(cirq.T(qubits[10]))
    circuit.append(cirq.CNOT(qubits[0], qubits[8]))
    circuit.append(cirq.H(qubits[1]))
    circuit.append(cirq.H(qubits[6]))
    circuit.append(cirq.CNOT(qubits[3], qubits[5]))
