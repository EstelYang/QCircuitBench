    circuit.append(cirq.H(qubits[7]))
    circuit.append(cirq.H(qubits[8]))
    circuit.append(cirq.H(qubits[9]))
    circuit.append((cu_gate ** (2 ** 0)).on(qubits[0], qubits[10]))
    circuit.append((cu_gate ** (2 ** 1)).on(qubits[1], qubits[10]))
    circuit.append((cu_gate ** (2 ** 2)).on(qubits[2], qubits[10]))
    circuit.append((cu_gate ** (2 ** 3)).on(qubits[3], qubits[10]))
    circuit.append((cu_gate ** (2 ** 4)).on(qubits[4], qubits[10]))
    circuit.append((cu_gate ** (2 ** 5)).on(qubits[5], qubits[10]))
    circuit.append((cu_gate ** (2 ** 6)).on(qubits[6], qubits[10]))
    circuit.append((cu_gate ** (2 ** 7)).on(qubits[7], qubits[10]))
    circuit.append((cu_gate ** (2 ** 8)).on(qubits[8], qubits[10]))
    circuit.append((cu_gate ** (2 ** 9)).on(qubits[9], qubits[10]))
    circuit.append(inverse_qft(qubits[:10]))
    circuit.append(cirq.measure(qubits[0], key='c0'))
    circuit.append(cirq.measure(qubits[1], key='c1'))
    circuit.append(cirq.measure(qubits[2], key='c2'))
    circuit.append(cirq.measure(qubits[3], key='c3'))
    circuit.append(cirq.measure(qubits[4], key='c4'))
    circuit.append(cirq.measure(qubits[5], key='c5'))
    circuit.append(cirq.measure(qubits[6], key='c6'))
    circuit.append(cirq.measure(qubits[7], key='c7'))
    circuit.append(cirq.measure(qubits[8], key='c8'))
    circuit.append(cirq.measure(qubits[9], key='c9'))
    return circuit
